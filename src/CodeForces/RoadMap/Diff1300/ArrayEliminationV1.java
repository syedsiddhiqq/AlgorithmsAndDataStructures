public class ArrayEliminationV1 {
    private static Scanner s = new Scanner(System.in);

    private static void solve() {
        System.out.println(Long.toBinaryString(s.nextLong()));
    }

    public static void main(String[] args) {
        int t = s.nextInt();
        while (t-- > 0) {
            solve();
        }
    }
}
